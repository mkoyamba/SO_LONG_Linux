#include "../../includes/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*result;

	result = malloc(sizeof(t_list));
	if (!result)
		return (NULL);
	result->next = NULL;
	result->content = content;
	return (result);
}
